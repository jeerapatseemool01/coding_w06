#include <stdio.h>

int main() {
    int a = 5, b = 2;
    float x = 3.0, y = 4.5;

    int r1 = a++ * b + (int)y % 3;
    // วิเคราะห์ทีละส่วน:
    // a++ ใช้ค่า a = 5 แล้วค่อยเพิ่มเป็น 6  5 * b = 5 * 2 = 10
    // (int)y % 3  (int)4.5 = 4  4 % 3 = 1
    // ดังนั้น r1 = 10 + 1 = 11
    printf("r1 = %d\n", r1); // r1 = 11
    int r2 = (a > b) && ((int)x / b < 2);
    // ตอนนี้ a = 6 (เพราะถูก ++ ไปแล้วจาก r1), b = 2, x = 3.0
    // (6 > 2) = true  1
    // (int)x / b  3 / 2 = 1 → 1 < 2 = true  1
    // 1 && 1 = 1  เป็นจริง
    printf("r2 = %d\n", r2); // r2 = 1
    float r3 = ++x * y - a / 2;
    // ++x  x = 3.0 + 1 = 4.0
    // 4.0 * y = 4.0 * 4.5 = 18.0
    // a = 6, a / 2 = 3 (หารแบบ int)
    // 18.0 - 3 = 15.0
    printf("r3 = %.2f\n", r3); // r3 = 15.00
    float r4 = ((x += 1.5) > y) || (b-- > 0);
    // x = 4.0 + 1.5 = 5.5  (5.5 > 4.5) = true  1
    // || เป็น short-circuit  ถ้าซ้ายเป็น true จะไม่เช็คขวา
    // b-- จะไม่ถูกใช้ (แต่ b ยังลดลงหลังจากนี้)
    // r4 = 1.0 (เพราะเก็บใน float)
    printf("r4 = %.2f\n", r4); // r4 = 1.00
    return 0;
}
