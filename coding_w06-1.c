#include <stdio.h>

int main() {
    int a = 5, b = 17;            // กำหนดค่าตัวแปรจำนวนเต็ม a = 5, b = 17
    float c = 8.5, d = 4.0;       // กำหนดค่าตัวแปรทศนิยม c = 8.5, d = 4.0
    printf("1. แสดงค่าทั้งหมด: a = %d, b = %d, c = %.1f, d = %.1f\n", a, b, c, d);
    // แสดงค่าทุกตัวแปร โดยใช้ %d สำหรับ int และ %.1f สำหรับ float
    printf("2. b + a = %d\n", b + a);
    // บวก b กับ a ได้ผลลัพธ์ 17 + 5 = 22
    printf("3. a - b = %d\n", a - b);
    // ลบ a - b ได้ผลลัพธ์ 5 - 17 = -12
    printf("4. c * d = %.2f\n", c * d);
    // คูณ float: 8.5 * 4.0 = 34.00 แสดง 2 ตำแหน่งทศนิยม
    printf("5. a * c = %.2f\n", a * c);
    // คูณ int กับ float: 5 * 8.5 = 42.50 แสดง 2 ตำแหน่งทศนิยม
    printf("6. c / d = %.2f\n", c / d);
    // หาร float: 8.5 / 4.0 = 2.12 (จริง ๆ คือ 2.125)
    printf("7. b / c = %.2f\n", b / c);
    // หาร int กับ float: 17 / 8.5 = 2.00
    printf("8. a %% b = %d\n", a % b);     
    // หาเศษจากการหาร 5 % 17 = 5 (ใช้ %% เพื่อ escape % ใน printf)
    printf("9. c %% a = %.2f\n", (float)((int)c % a));  
    // แปลง c = 8.5 เป็น int คือ 8 แล้วหา 8 % 5 = 3 → แสดงผลเป็น 3.00
    printf("10. c %% d = %.2f\n", (float)((int)c % (int)d));
    // แปลงทั้ง c และ d เป็น int → 8 % 4 = 0 → แสดงผลเป็น 0.00

    return 0;
}

// หากเขียน c % a โดยตรงจะเกิด error เพราะเครื่องหมาย % ใช้ได้กับ int เท่านั้น ไม่รองรับ float